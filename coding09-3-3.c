#include <stdio.h>

int main() {
    // 1. กำหนดค่าเริ่มต้นนอกลูป
    int i = 1;

    do {
        // ถ้า i มีค่าเท่ากับ 6
        if (i == 6) {
            // 3. ต้องเพิ่มค่า i ก่อนที่จะ continue
            // เพื่อป้องกันการเกิด infinite loop
            i++;
            continue; // ข้ามการทำงานที่เหลือ แล้วไปตรวจสอบเงื่อนไขท้ายลูป
        }
        // แสดงผลเมื่อ i ไม่ใช่ 6
        printf("%d ", i);

        // 3. เพิ่มค่า i สำหรับรอบปกติ
        i++;
    } while (i <= 10); // 2. ตรวจสอบเงื่อนไขหลังจบการทำงานในแต่ละรอบ

    printf("\n");
    return 0;
}

//* while และ do-while loop: มีข้อควรระวังสำคัญ! การเพิ่มค่า (i++) เป็นคำสั่งที่อยู่ ภายใน บล็อกของลูปเอง ดังนั้นถ้า i มีค่าเป็น 6 แล้วเราสั่ง continue 
//โดยที่ยังไม่ได้เพิ่มค่า i ก่อน โปรแกรมจะข้ามคำสั่ง i++ ที่อยู่ท้ายลูปไป ทำให้ i ยังคงเป็น 6 และเมื่อวนกลับมาตรวจสอบเงื่อนไขใหม่ ก็จะเข้า if (i == 6) และ continue อีกครั้ง 
//วนซ้ำไปเรื่อยๆ ไม่รู้จบ (เรียกว่า Infinite Loop) ดังนั้น เราจึงจำเป็นต้องใส่ i++ ไว้ในบล็อก if ก่อนที่จะเรียก continue

//สรุปผล
//for loop มีโครงสร้างที่รวบการกำหนดค่าเริ่มต้น, เงื่อนไข, และการปรับปรุงค่าไว้ในบรรทัดเดียว ทำให้การใช้ continue ค่อนข้างตรงไปตรงมาและปลอดภัย
// while และ do-while loop ให้อิสระในการวางตำแหน่งของโค้ดปรับปรุงค่า 
//แต่ก็เพิ่มความรับผิดชอบให้โปรแกรมเมอร์ต้องจัดการลำดับการทำงานให้ถูกต้องเพื่อหลีกเลี่ยงข้อผิดพลาดอย่าง Infinite Loop เมื่อใช้ร่วมกับคำสั่ง continue