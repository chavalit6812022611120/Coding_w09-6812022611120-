#include <stdio.h>

int main() {
    int sum, p, x, y;
    sum = 0;
    for (x = 1, y = 1; x * y <= 15; x++, y += 2)
    {
        p = x * y;
        sum = sum + p;
        printf("%d * %d = %d\n", x, y, p);
    }
    printf("summation of x * y = %d\n", sum);
    return 0;
}

//สรุปและอภิปรายผลการทดลอง
//โปรแกรมนี้สาธิตการทำงานของ for loop ที่มีเงื่อนไขค่อนข้างซับซ้อน โดยลูปจะหยุดทำงานเมื่อผลคูณของตัวแปร x และ y (ซึ่งมีค่าเพิ่มขึ้นไม่เท่ากันในแต่ละรอบ) มีค่าเกิน 15
//ผลลัพธ์สุดท้ายคือ 22 ซึ่งมาจากผลรวมของผลคูณใน 3 รอบที่เงื่อนไขเป็นจริง (1 + 6 + 15 = 22)

//อธิบายการทำงาน
//รอบที่ 1: x=1, y=1, เงื่อนไข 1*1 <= 15 เป็นจริง, sum กลายเป็น 1, พิมพ์ 1 * 1 = 1
 //รอบที่ 2: x=2, y=3, เงื่อนไข 2*3 <= 15 เป็นจริง, sum กลายเป็น 7, พิมพ์ 2 * 3 = 6
 //รอบที่ 3: x=3, y=5, เงื่อนไข 3*5 <= 15 เป็นจริง, sum กลายเป็น 22, พิมพ์ 3 * 5 = 15
 //สิ้นสุดลูป: x=4, y=7, เงื่อนไข 4*7 <= 15 เป็นเท็จ, โปรแกรมจึงออกจากลูป
 //แสดงผล: พิมพ์ค่า sum สุดท้ายคือ summation of x * y = 22